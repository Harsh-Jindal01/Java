package basis;

public class casting {
  public static void main(String[] args) {
      
    double price = 100.00;
    double finalPrice = price + 18;

    System.out.println(finalPrice);

    //Explicit casting

    int value = 100;
    int fValue = value + (int)18.18;

    System.out.println(fValue);

  }
}
